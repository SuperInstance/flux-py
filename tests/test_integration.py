# 1. FluxVM execution tests (never had pytest coverage before)
# ═════════════════════════════════════════════════════════════════════════════

class TestFluxVMExecution(unittest.TestCase):
    """Verify FluxVM execution correctness across all opcodes."""

    def test_halt_empty(self):
        vm = FluxVM(bytes([0x80]))
        vm.execute()
        self.assertTrue(vm.halted)
        self.assertIsNone(vm.error)

    def test_movi(self):
        bc = bytes([0x2B, 0x00, 0x2A, 0x00]) + bytes([0x80])  # MOVI R0,42; HALT
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 42)

    def test_mov(self):
        bc = bytes([0x2B, 0x01, 0x63, 0x00])  # MOVI R1, 99
        bc += bytes([0x2C, 0x00, 0x01])        # MOV R0, R1
        bc += bytes([0x80])                      # HALT
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 99)

    def test_iadd(self):
        # MOVI R0,7; MOVI R1,3; IADD R0,R0,R1; HALT
        bc = bytes([0x2B, 0x00, 0x07, 0x00,
                     0x2B, 0x01, 0x03, 0x00,
                     0x08, 0x00, 0x00, 0x01,
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 10)

    def test_isub(self):
        bc = bytes([0x2B, 0x00, 0x0A, 0x00,  # MOVI R0, 10
                     0x2B, 0x01, 0x03, 0x00,  # MOVI R1, 3
                     0x09, 0x00, 0x00, 0x01,  # ISUB R0,R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 7)

    def test_imul(self):
        bc = bytes([0x2B, 0x00, 0x06, 0x00,  # MOVI R0, 6
                     0x2B, 0x01, 0x07, 0x00,  # MOVI R1, 7
                     0x0A, 0x02, 0x00, 0x01,  # IMUL R2,R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(2), 42)

    def test_idiv(self):
        bc = bytes([0x2B, 0x00, 0x14, 0x00,  # MOVI R0, 20
                     0x2B, 0x01, 0x04, 0x00,  # MOVI R1, 4
                     0x0B, 0x02, 0x00, 0x01,  # IDIV R2,R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(2), 5)

    def test_idiv_by_zero(self):
        bc = bytes([0x2B, 0x00, 0x2A, 0x00,  # MOVI R0, 42
                     0x0B, 0x02, 0x00, 0x01,  # IDIV R2,R0,R1 (R1=0)
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertIsNotNone(vm.error)
        self.assertIn("Division by zero", vm.error)

    def test_inc(self):
        bc = bytes([0x2B, 0x00, 0x05, 0x00,  # MOVI R0, 5
                     0x0E, 0x00,              # INC R0
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 6)

    def test_dec(self):
        bc = bytes([0x2B, 0x00, 0x05, 0x00,  # MOVI R0, 5
                     0x0F, 0x00,              # DEC R0
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 4)

    def test_cmp_less(self):
        bc = bytes([0x2B, 0x00, 0x03, 0x00,  # MOVI R0, 3
                     0x2B, 0x01, 0x05, 0x00,  # MOVI R1, 5
                     0x0D, 0x00, 0x01,        # CMP R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(13), -1)

    def test_cmp_equal(self):
        bc = bytes([0x2B, 0x00, 0x07, 0x00,  # MOVI R0, 7
                     0x2B, 0x01, 0x07, 0x00,  # MOVI R1, 7
                     0x0D, 0x00, 0x01,        # CMP R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(13), 0)

    def test_cmp_greater(self):
        bc = bytes([0x2B, 0x00, 0x09, 0x00,  # MOVI R0, 9
                     0x2B, 0x01, 0x04, 0x00,  # MOVI R1, 4
                     0x0D, 0x00, 0x01,        # CMP R0,R1
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(13), 1)

    def test_jnz_taken(self):
        """JNZ when register is non-zero should jump (back to a label)."""
        # Use assembler to get correct relative offsets
        bc = Assembler().assemble(
            "MOVI R0, 1\nMOVI R1, 0\nloop: JNZ R0, loop\nMOVI R1, 99\nHALT"
        )
        # JNZ loops back forever → max_cycles should stop it
        vm = FluxVM(bc, max_cycles=100)
        vm.execute()
        self.assertFalse(vm.halted)
        self.assertEqual(vm.cycles, 100)

    def test_jnz_not_taken(self):
        """JNZ when register is zero should NOT jump."""
        bc = bytes([0x2B, 0x00, 0x00, 0x00,  # MOVI R0, 0
                     0x2B, 0x01, 0x01, 0x00,  # MOVI R1, 1
                     0x06, 0x00, 0x01, 0x00,  # JNZ R0, +1 (skip HALT)
                     0x80])                      # HALT
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(1), 1)
        self.assertTrue(vm.halted)

    def test_jz_taken(self):
        """JZ when register is zero should jump."""
        # Use assembler with label for correct offset
        bc = Assembler().assemble(
            "MOVI R0, 0\nMOVI R1, 5\nJZ R0, skip\nMOVI R1, 99\nskip: HALT"
        )
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(1), 5)
        self.assertTrue(vm.halted)

    def test_jmp(self):
        """JMP unconditionally jumps."""
        # Use assembler with label for correct offset
        bc = Assembler().assemble(
            "MOVI R0, 1\nJMP skip\nMOVI R0, 2\nskip: HALT"
        )
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(0), 1)
        self.assertTrue(vm.halted)

    def test_push_pop(self):
        bc = bytes([0x2B, 0x00, 0x2A, 0x00,  # MOVI R0, 42
                     0x01, 0x00,              # PUSH R0
                     0x2B, 0x01, 0x00, 0x00,  # MOVI R1, 0
                     0x02, 0x02,              # POP R2
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(2), 42)

    def test_pop_empty_stack(self):
        bc = bytes([0x02, 0x00, 0x80])  # POP R0; HALT
        vm = FluxVM(bc)
        vm.execute()
        self.assertIsNotNone(vm.error)
        self.assertIn("Stack underflow", vm.error)

    def test_push_pop_lifo(self):
        bc = bytes([0x2B, 0x00, 0x01, 0x00,  # MOVI R0, 1
                     0x2B, 0x01, 0x02, 0x00,  # MOVI R1, 2
                     0x01, 0x00,              # PUSH R0
                     0x01, 0x01,              # PUSH R1
                     0x02, 0x02,              # POP R2
                     0x02, 0x03,              # POP R3
                     0x80])
        vm = FluxVM(bc)
        vm.execute()
        self.assertEqual(vm.reg(2), 2)  # LIFO: last pushed = first popped
        self.assertEqual(vm.reg(3), 1)

    def test_max_cycles_protection(self):
        """VM should stop when PC exceeds bytecode without HALT."""
        # MOVI R0, 1 → single instruction, no HALT
        # VM exits because pc >= len(bc), not because of max_cycles
        bc = bytes([0x2B, 0x00, 0x01, 0x00])
        vm = FluxVM(bc, max_cycles=5)
        vm.execute()
        self.assertFalse(vm.halted)
        self.assertLessEqual(vm.cycles, 5)
        self.assertIsNone(vm.error)

    def test_result_returns_none_on_error(self):
        bc = bytes([0x02, 0x00])  # POP R0 — no stack
        vm = FluxVM(bc)
        vm.execute()
        self.assertIsNone(vm.result(0))

    def test_registers_initialized_zero(self):
        vm = FluxVM(bytes([0x80]))
        vm.execute()
        for i in range(16):
            self.assertEqual(vm.reg(i), 0)


