class TestTranslationSemantics(unittest.TestCase):
    """Verify programs produce equivalent results across translation."""

    def _run_v1(self, asm_text: str, result_reg: int = 0) -> int:
        bc = Assembler().assemble(asm_text)
        vm = FluxVM(bc)
        vm.execute()
        return vm.reg(result_reg)

    def test_translate_v1_to_v2_preserves_operands(self):
        """After v1→v2 translation, MOVI immediate is intact."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()
        t = ISATranslator(v1, v2)
        bc = make_v1_bytecode(("MOVI", 0, 42), ("HALT",))
        result = t.translate(bc)
        out = result.bytecode
        # Operand bytes should be identical: [rd=0x00, i16_lo, i16_hi]
        self.assertEqual(out[1], 0x00)  # R0
        val = struct.unpack("<h", out[2:4])[0]
        self.assertEqual(val, 42)

    def test_translate_preserves_register_operands(self):
        """Register operands survive translation (e.g., IADD R3,R5,R7)."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()
        t = ISATranslator(v1, v2)
        bc = make_v1_bytecode(("IADD", 3, 5, 7), ("HALT",))
        result = t.translate(bc)
        out = result.bytecode
        # Second byte should still be R3=0x03, etc.
        self.assertEqual(out[1], 3)
        self.assertEqual(out[2], 5)
        self.assertEqual(out[3], 7)

    def test_round_trip_byte_identity(self):
        """v1→v2→v1 round-trip must produce identical bytes."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()
        t_fwd = ISATranslator(v1, v2)
        t_back = ISATranslator(v2, v1)
        original = make_v1_bytecode(
            ("MOVI", 0, 10), ("IADD", 0, 0, 0), ("DEC", 0), ("HALT",)
        )
        translated = t_fwd.translate(original).bytecode
        roundtrip = t_back.translate(translated).bytecode
        self.assertEqual(roundtrip, original)

    def test_round_trip_register_semantics(self):
        """After v1→v2→v1 round-trip, VM execution must be identical."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()
        t_fwd = ISATranslator(v1, v2)
        t_back = ISATranslator(v2, v1)

        # Build original v1 bytecode from assembler
        asm_text = "MOVI R0, 10\nMOVI R1, 3\nIADD R0, R0, R1\nHALT"
        original_bc = Assembler().assemble(asm_text)

        # Execute original
        vm1 = FluxVM(original_bc)
        vm1.execute()
        result_before = vm1.reg(0)

        # Round-trip
        translated = t_fwd.translate(original_bc).bytecode
        roundtrip = t_back.translate(translated).bytecode

        # Execute round-tripped
        vm2 = FluxVM(roundtrip)
        vm2.execute()
        result_after = vm2.reg(0)

        self.assertEqual(result_before, result_after)
        self.assertEqual(result_before, 13)

    def test_translation_coverage_matches_registry_score(self):
        """TranslationResult.coverage should align with ConformanceVector score."""
        v1 = ISADialect.v1()
        v2 = ISADialect.v2()
        cv = ConformanceVector(v1, v2)
        report = cv.report()

        # Build a program using all v1 opcodes
        bc = make_v1_bytecode(
            ("MOVI", 0, 1), ("IADD", 0, 0, 1), ("IMUL", 0, 0, 0),
            ("IDIV", 0, 0, 1), ("DEC", 0), ("JNZ", 0, 0), ("HALT",)
        )

        t = ISATranslator(v1, v2)
        result = t.translate(bc)

        # Program with all portable ops should have 100% coverage
        self.assertEqual(result.coverage, 1.0)
        # Score should reflect full portability of v1→v2
        self.assertGreaterEqual(report.score, 0.85)

    def test_unmapped_program_vm_fails(self):
        """A v2-only program (SUB) translated to v1 should leave unmapped 0xFF."""
        v2, v1 = ISADialect.v2(), ISADialect.v1()
        t = ISATranslator(v2, v1, strict=False)
        # SUB R0, R0, R0 (v2 only)
        bc = bytes([0x21, 0x00, 0x00, 0x00, 0x00])  # SUB + HALT
        result = t.translate(bc)
        self.assertEqual(result.unmapped, 1)
        self.assertEqual(result.bytecode[0], 0xFF)  # placeholder


