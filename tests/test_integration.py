class TestDisassemblerConformance(unittest.TestCase):
    """Verify disassembler output aligns with ISA dialect definitions."""

    def test_disassemble_v1_produces_v1_mnemonics(self):
        """Disassembler should produce mnemonics that exist in v1 dialect."""
        asm = Assembler()
        dis = Disassembler()
        v1 = ISADialect.v1()

        bc = asm.assemble("MOVI R0, 42\nIADD R0, R0, R1\nDEC R0\nHALT")
        text = dis.disassemble(bc)

        # Extract mnemonics from disassembly output
        for line in text.strip().split("\n"):
            parts = line.split(": ", 1)
            if len(parts) < 2:
                continue
            instr = parts[1].strip().split()[0]
            if instr.startswith("???"):
                continue
            # Mnemonic should exist in v1 dialect
            self.assertIsNotNone(
                v1.opcode_for(instr),
                f"Disassembler produced unknown mnemonic '{instr}'"
            )

    def test_disassemble_all_v1_opcodes(self):
        """Every v1 opcode should disassemble correctly."""
        dis = Disassembler()
        v1 = ISADialect.v1()

        # Build bytecode for each v1 opcode
        test_cases = {
            "HALT": bytes([0x80]),
            "DEC":  bytes([0x0F, 0x00]),
            "IADD": bytes([0x08, 0x00, 0x01, 0x02]),
            "IMUL": bytes([0x0A, 0x00, 0x01, 0x02]),
            "IDIV": bytes([0x0B, 0x00, 0x01, 0x02]),
            "MOVI": bytes([0x2B, 0x00, 0x01, 0x00]),
            "JNZ":  bytes([0x06, 0x00, 0x01, 0x00]),
        }

        for mn, bc in test_cases.items():
            with self.subTest(mnemonic=mn):
                text = dis.disassemble(bc)
                self.assertIn(mn, text)

