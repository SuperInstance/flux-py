# ─── Helpers ─────────────────────────────────────────────────────────────────

def make_v1_bytecode(*instructions) -> bytes:
    """Build v1 bytecode from (mnemonic, *operands) tuples."""
    v1 = ISADialect.v1()
    buf = bytearray()
    for item in instructions:
        if isinstance(item, (list, tuple)):
            mn = item[0]
            operands = item[1:]
        else:
            mn = item
            operands = []
        op = v1.opcode_for(mn)
        assert op is not None, f"Unknown mnemonic: {mn}"
        buf.append(op)
        if mn == "HALT":
            pass
        elif mn == "DEC":
            buf.append(operands[0])
        elif mn in ("IADD", "IMUL", "IDIV"):
            buf.extend(operands[:3])
        elif mn == "MOVI":
            buf.append(operands[0])
            buf.extend(struct.pack("<h", operands[1]))
        elif mn == "JNZ":
            buf.append(operands[0])
            buf.extend(struct.pack("<h", operands[1]))
    return bytes(buf)


def assemble_and_run(asm_text: str, result_reg: int = 0):
    """Assemble v1 assembly text and run on FluxVM. Returns register value."""
    bc = Assembler().assemble(asm_text)
    vm = FluxVM(bc)
    vm.execute()
    return vm.reg(result_reg)


