class TestAssemblerConformance(unittest.TestCase):
    """Verify that bytecode produced by the Assembler conforms to v1 dialect."""

    def test_simple_program_conforms(self):
        asm = Assembler()
        bc = asm.assemble("MOVI R0, 42\nHALT")
        v1 = ISADialect.v1()
        issues = v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_complex_program_conforms(self):
        asm_text = (
            "MOVI R0, 10\nMOVI R1, 3\n"
            "IADD R2, R0, R1\n"
            "IMUL R2, R2, R0\n"
            "DEC R2\n"
            "HALT"
        )
        bc = Assembler().assemble(asm_text)
        v1 = ISADialect.v1()
        issues = v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_loop_program_conforms(self):
        asm_text = (
            "MOVI R0, 5\nMOVI R1, 0\n"
            "loop: IADD R1, R1, R0\n"
            "DEC R0\nJNZ R0, loop\n"
            "HALT"
        )
        bc = Assembler().assemble(asm_text)
        v1 = ISADialect.v1()
        issues = v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_v1_dialect_opcodes_conform(self):
        """Program using only v1-dialect opcodes should conform to v1."""
        # v1 dialect: MOVI, IADD, IMUL, IDIV, DEC, JNZ, HALT
        asm_text = (
            "MOVI R0, 1\nMOVI R1, 1\n"
            "IADD R0, R0, R1\n"
            "IMUL R2, R0, R1\n"
            "IDIV R3, R0, R1\n"
            "DEC R0\n"
            "JNZ R1, 0\n"
            "HALT"
        )
        bc = Assembler().assemble(asm_text)
        v1 = ISADialect.v1()
        issues = v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_vm_only_opcodes_not_in_v1_dialect(self):
        """VM-only opcodes (ISUB, INC, MOV, CMP, JZ, JMP, PUSH, POP) \
        are NOT in the v1 ISA dialect."""
        v1 = ISADialect.v1()
        vm_only = ["ISUB", "INC", "MOV", "CMP", "JZ", "JMP", "PUSH", "POP"]
        for mn in vm_only:
            self.assertIsNone(v1.opcode_for(mn),
                f"{mn} should not be in v1 dialect")

    def test_vm_only_opcodes_fail_v1_validation(self):
        """Bytecode with VM-only opcodes should not conform to v1 dialect."""
        asm_text = "MOVI R0, 1\nISUB R0, R0, R1\nHALT"
        bc = Assembler().assemble(asm_text)
        v1 = ISADialect.v1()
        issues = v1.validate_bytecode(bc)
        self.assertGreater(len(issues), 0)

    def test_assembled_bytecode_not_v2_conformant(self):
        """v1 assembler output should NOT fully conform to v2 dialect."""
        bc = Assembler().assemble("MOVI R0, 42\nHALT")
        v2 = ISADialect.v2()
        issues = v2.validate_bytecode(bc)
        self.assertGreater(len(issues), 0)

    def test_assembler_bytecode_size_matches_isa(self):
        """Assembler instruction sizes must match ISA format table."""
        asm_text = "MOVI R0, 1\nIADD R0, R0, R0\nDEC R0\nHALT"
        bc = Assembler().assemble(asm_text)
        # MOVI=4, IADD=4, DEC=2, HALT=1 → 11 bytes
        self.assertEqual(len(bc), 11)


