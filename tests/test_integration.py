class TestInterpreterConformance(unittest.TestCase):
    """Verify that Interpreter-generated bytecode conforms to v1 ISA."""

    def setUp(self):
        self.interp = Interpreter()
        self.v1 = ISADialect.v1()

    def test_addition_bytecode_conforms(self):
        result, msg = self.interp.run("compute 7 + 5")
        # We need to check the bytecode directly
        vocab = self.interp.vocab
        match = vocab.match("compute 7 + 5")
        self.assertIsNotNone(match)
        asm = match['assembly'].replace('${a}', '7').replace('${b}', '5')
        bc = Assembler().assemble(asm)
        issues = self.v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_factorial_bytecode_conforms(self):
        vocab = self.interp.vocab
        match = vocab.match("factorial of 5")
        self.assertIsNotNone(match)
        asm = match['assembly'].replace('${n}', '5')
        bc = Assembler().assemble(asm)
        issues = self.v1.validate_bytecode(bc)
        self.assertEqual(len(issues), 0)

    def test_v1_only_vocab_patterns_conform(self):
        """Vocabulary patterns using only v1-dialect opcodes must produce \
        valid v1 bytecode."""
        vocab = Vocabulary().get_builtin()
        v1 = ISADialect.v1()

        # Only patterns that exclusively use v1 opcodes \
        # (MOVI, IADD, IMUL, IDIV, DEC, JNZ, HALT)
        v1_only_patterns = [
            "compute 3 + 4",      # MOVI, IADD
            "compute 6 * 7",      # MOVI, IMUL
            "compute 20 / 4",     # MOVI, IDIV
            "factorial of 5",     # MOVI, IMUL, DEC, JNZ
            "double 21",          # MOVI, IADD
            "square 7",           # MOVI, IMUL
            "power of 2 to 8",    # MOVI, IMUL, DEC, JNZ
            "hello",              # MOVI
        ]

        for text in v1_only_patterns:
            with self.subTest(text=text):
                match = vocab.match(text)
                self.assertIsNotNone(match, f"No vocab match for: {text}")
                # Substitute template variables
                asm = match['assembly']
                for k, v in match['groups'].items():
                    asm = asm.replace('${' + k + '}', str(v))
                bc = Assembler().assemble(asm)
                issues = v1.validate_bytecode(bc)
                self.assertEqual(len(issues), 0,
                    f"Vocab pattern '{text}' produced invalid v1 bytecode")

    def test_extended_vocab_patterns_use_vm_only_opcodes(self):
        """Some vocabulary patterns use opcodes beyond v1 dialect \
        (ISUB, MOV, JZ, CMP, JMP) — expected behavior."""
        vocab = Vocabulary().get_builtin()
        v1 = ISADialect.v1()

        extended_patterns = [
            "compute 10 - 2",     # uses ISUB
            "fibonacci of 7",     # uses MOV
            "sum 1 to 10",        # uses CMP, JZ, JMP, INC
        ]

        for text in extended_patterns:
            with self.subTest(text=text):
                match = vocab.match(text)
                self.assertIsNotNone(match)
                asm = match['assembly']
                for k, v in match['groups'].items():
                    asm = asm.replace('${' + k + '}', str(v))
                bc = Assembler().assemble(asm)
                issues = v1.validate_bytecode(bc)
                # These patterns use VM-only opcodes, so they should NOT \
                # fully conform to v1
                self.assertGreater(len(issues), 0,
                    f"'{text}' should use VM-only opcodes not in v1 dialect")

    def test_interpreter_results_correct(self):
        """Interpreter should produce correct results for all patterns."""
        interp = Interpreter()
        test_cases = [
            ("compute 3 + 4", 7),
            ("compute 10 - 2", 8),
            ("compute 6 * 7", 42),
            ("compute 20 / 4", 5),
            ("factorial of 5", 120),
            ("fibonacci of 7", 13),
            ("double 21", 42),
            ("square 7", 49),
            ("sum 1 to 10", 55),
            ("power of 2 to 8", 256),
            ("hello", 42),
        ]
        for text, expected in test_cases:
            with self.subTest(text=text):
                result, msg = interp.run(text)
                self.assertEqual(result, expected,
                    f"Interpreter returned {result}, expected {expected} for '{text}'")
                self.assertIn("OK", msg)


