# 9. In-place translation ↔ VM execution
# ═════════════════════════════════════════════════════════════════════════════

class TestInPlaceTranslationVM(unittest.TestCase):
    """Verify in-place translation results can still be executed correctly."""

    def test_inplace_translate_then_execute_roundtrip(self):
        """Translate v1→v2→v1 in-place, then execute on VM."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()

        # Build a v1 program: MOVI R0,10; MOVI R1,3; IADD R0,R0,R1; HALT
        bc = bytearray(make_v1_bytecode(
            ("MOVI", 0, 10), ("MOVI", 1, 3), ("IADD", 0, 0, 1), ("HALT",)
        ))

        # Execute original
        vm_before = FluxVM(bytes(bc))
        vm_before.execute()
        r0_before = vm_before.reg(0)

        # Translate v1→v2 in-place
        t_fwd = ISATranslator(v1, v2)
        result_fwd = t_fwd.translate_inplace(bc)
        self.assertTrue(result_fwd.success)

        # Translate v2→v1 in-place (back to v1 opcodes)
        t_back = ISATranslator(v2, v1)
        result_back = t_back.translate_inplace(bc)
        self.assertTrue(result_back.success)

        # Execute after round-trip
        vm_after = FluxVM(bytes(bc))
        vm_after.execute()
        r0_after = vm_after.reg(0)

        self.assertEqual(r0_before, r0_after)
        self.assertEqual(r0_before, 13)

    def test_inplace_preserves_byte_length_for_vm(self):
        """In-place translation must not change bytecode length (VM assumes fixed layout)."""
        v1, v2 = ISADialect.v1(), ISADialect.v2()
        original = make_v1_bytecode(
            ("MOVI", 0, 1), ("IADD", 0, 0, 1), ("DEC", 0), ("HALT",)
        )
        bc = bytearray(original)
        ISATranslator(v1, v2).translate_inplace(bc)
        self.assertEqual(len(bc), len(original))

    def test_nonstrict_inplace_unmapped_preserves_layout(self):
        """Non-strict in-place translation of unmapped opcodes preserves operand layout."""
        v2, v1 = ISADialect.v2(), ISADialect.v1()
        # SUB R0,R1,R2 in v2 → unmapped in v1 → 0xFF + operands preserved
        bc = bytearray([0x21, 0x00, 0x01, 0x02, 0x00])  # SUB + HALT
        t = ISATranslator(v2, v1, strict=False)
        result = t.translate_inplace(bc)
        self.assertEqual(result.unmapped, 1)
        self.assertEqual(bc[0], 0xFF)  # placeholder
        self.assertEqual(bc[1], 0x00)  # R0 preserved
        self.assertEqual(bc[2], 0x01)  # R1 preserved
        self.assertEqual(bc[3], 0x02)  # R2 preserved
        self.assertEqual(bc[4], 0x80)  # HALT translated fine


